NESTE ATO denominado (s) LOCADOR, JOSE BRAGA DE ALMEIDA, brasileiro, casado, aposentado, CPF 984.771.888-15, RG 14055414-2 , residente e domiciliada na Rua Minas Gerais, 788 – casa frente – Bairro Jacaré – Cabreúva/SP.
DE OUTRO LADO, denominado (s) LOCATÁRIO, REGINALDO VENANCIO BRAGA, brasileiro, casado, motorista, CPF 262.361.808-90 e RG 33.340.523-7.
O IMÓVEL RESIDENCIAL de propriedade do LOCADOR situa-se na Rua Minas Gerais, nº788 casa fundos – Bairro Jacaré – Cabreúva/SP
O PRAZO da locação do imóvel mencionado acima terá validade de 24 meses, iniciando no dia 08 do mês de fevereiro do ano de 2016 e terminando no dia 08 do mês fevereiro do ano de 2018.
A presente LOCAÇÃO destina-se ao uso do imóvel somente para fins residenciais, estando o LOCATÁRIO proibido de sublocá-lo ou usá-lo de forma diferente do previsto, sem a devida autorização do PROPRIETÁRIO ou LOCADOR.
O LOCADOR recebe do LOCATÁRIO, no ato da assinatura deste contrato, o valor de R$ 800,00 (oitocentos reais), referente a 1 (um) aluguel adiantada, uma vez que este contrato não conta com avalistas. Havendo a desocupação do imóvel, o LOCADOR confirmar que não há nenhum prejuízo material ou inadimplência do aluguel, da conta de água e luz.
O valor do aluguel é de R$ 800,00 (oitocentos reais), que o LOCATÁRIO deverá pagar ao LOCADOR . 
O Pagamento no valor acima devera ser feito todo dia 10 de cada mês. Será cobrada multa de 10% para pagamento feito após prazo.
O LOCATÁRIO responsabiliza-se de pagar todos os serviços públicos e despesas ligadas à conservação do imóvel, tais como, taxa de luz, saneamento, esgoto, telefone, etc.
Caso o LOCATÁRIO venha a quebrar pia, gabinete, janelas, portas, Box do banheiro ou outros itens da casa, ele deverá pagar sua troca ou conserto.
Caso uma RESCISÃO do contrato de aluguel seja necessária, ambas as partes concordam em dar um prazo de 30 dias de aviso prévio e o responsável pela rescisão deverá pagar multa/indenização de três alugueis.
RESCISÃO imediata do presente contrato, sem prejuízo dos numerários previstos neste, ocorrerá caso aconteça qualquer sinistro que venha a impossibilitar o uso do imóvel, no caso de desapropriação do mesmo.
O LOCADOR concorda que o objeto de locação será entregue limpo e em condições de perfeito funcionamento, tais como instalações elétricas e hidráulicas, portas, portões e acessórios, paredes pintadas, cômodos em bom estado e com todas as despesas e tributos pagos. 
O LOCATÁRIO assume responsabilidade em manter o imóvel locado desta forma e o restituirá nas mesmas condições, salvo a deterioração decorrente do uso normal do mesmo.
LOCATÁRIO e LOCADOR elegem o Foro Comarca de Cabreúva/SP, onde está situado o imóvel, para dirimirem quaisquer dúvidas provenientes da execução e cumprimento do presente contrato.
De maneira acordada e justa, LOCATÁRIO e LOCADOR, assinam, juntamente com 2 testemunha (s) o presente INSTRUMENTO PARTICULAR DE LOCAÇÃO RESIDENCIAL, que passa a vigorar e fica desde agora aceito, pelas cláusulas acima descritas.

